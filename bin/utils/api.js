module.exports.generateKey = function () {
    let d = new Date().getTime();

    return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function(c) {
        let r = (d + Math.random()*16)%16 | 0;
        d = Math.floor(d/16);

        return ('x' === c ? r : (r&0x3|0x8)).toString(16);
    });
};
